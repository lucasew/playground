---@param x number
---@param y number
---@return number
function add(x, y)
    return x + y
end

---@param x number
---@param y number
---@return number
function sub(x, y)
    return x - y
end

---@param x number
---@param y number
---@return number
function mul(x, y)
    return x * y
end

---@param x number
---@param y number
---@return number
function div(x, y)
    return x / y
end
